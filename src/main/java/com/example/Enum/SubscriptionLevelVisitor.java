package com.example.Enum;

public enum SubscriptionLevelVisitor {

    FREE, // юзер может осматривать фильмы их трейлера и новости
    GOLD, // доступ как у FREE + юзер покупает подписку на свободный просмотр всех фильмов на платформе
    PLATINUM // доступ как у GOLD + юзер получает возможность узнавать детально информацию о каждом фильме и его етапах сьемки

}
